    public function updatePassword(Request $request)
    {


        $rules = [
            'mypassword' => 'required',
            'password' => 'required|confirmed|min:8|max:55',
        ];

        $messages = [
            'mypassword.required' => 'El campo es requerido',
            'password.required' => 'El campo es requerido',
            'password.confirmed' => 'Los passwords no coinciden',
            'password.min' => 'El mínimo permitido son 6 caracteres',
            'password.max' => 'El máximo permitido son 18 caracteres',
        ];

        $validator = Validator::make($request->all(), $rules, $messages);
        if ($validator->fails()){
            return redirect('profile/editpassword')->withErrors($validator);
        }
        else{
            if (Hash::check($request->mypassword, Auth::user()->password)){
                $user = new User;
                $user->where('email', '=', Auth::user()->email)
                     ->update(['password' => bcrypt($request->password)]);
                return redirect('profile/edit')->with('message', 'Password cambiado con éxito');
            }
            else
            {
                return redirect('profile/editpassword')->with('status', 'Credenciales incorrectas');
            }
        }
    }

